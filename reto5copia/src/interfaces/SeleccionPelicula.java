    private void SerieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SerieActionPerformed
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/netentertainment",
                            "root", "Auto1921*");
            Statement statement;
            statement = con.createStatement();

            ResultSet resultSet;
            resultSet = statement.executeQuery(
                "Select con_titulo from contenido inner join serie on con_id = serie.ser_id");

            Lista.removeAllItems();
            String Titulo;
            while(resultSet.next()) {
                Titulo = resultSet.getString("con_titulo");
                Lista.addItem(Titulo);

            }

            resultSet.close();
            statement.close();
            con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_SerieActionPerformed

